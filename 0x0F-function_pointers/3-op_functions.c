#include "3-calc.h"

/**
* op_add - returns sum
* @a: input number a
* @b: input integer b
* Return: returns sum
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - returns difference
* @a: input number a
* @b: input integer b
* Return: returns difference
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - returns product
* @a: input number a
* @b: input integer b
* Return: returns prodcut
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div- returns division
* @a: input number a
* @b: input integer b
* Return: returns div
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - returns remainder
* @a: input number a
* @b: input integer b
* Return: returns moduli
*/
int op_mod(int a, int b)
{
return (a % b);
}
