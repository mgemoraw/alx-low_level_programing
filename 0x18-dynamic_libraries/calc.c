#include "calc.h"
/**
 * add - adds to numbers
 * @a: input number a
 * @b: input number b
 * Return: retuns sum or a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts b from a
 * @a: input number a
 * @b: input number b
 * Return: returns a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns a * b
 * @a: input number a
 * @b: input number b
 * Return: returns a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - returns division of a by b
 * @a: input number a
 * @b: input number b
 * Return: rutns a/b
 */
int div(int a, int b)
{
	return (a/b);
}

/**
 * mod - returns the moduli of a by b
 * @a: input number a
 * @b: input number b
 * Return: returns a % b
 */
int mod(int a, int b)
{
	return (a % b);
}

